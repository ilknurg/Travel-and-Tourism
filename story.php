<p>The history of Istanbul explains the historical development of the modern city of Istanbul.
The settlement was founded by Thracian tribes between the 13th and 11th centuries BC,[1][2] whose earliest known name is Lygos.[3] It was colonised by the Greeks in the 7th century BC. It fell to the Roman Republic in AD 196, and was known as Byzantium until 330, when it was renamed Constantinople and made the new capital of the Roman Empire. During late antiquity, the city rose to be the largest of the western hemisphere, with a population peaking at close to half a million people. Constantinople was the capital of the Eastern Roman Empire, known as the Byzantine Empire. That ended with the Muslim conquest in 1453. Constantinople then became the capital of the Ottoman Empire.
Population had declined during the medieval period, but as the Ottoman Empire approached its historical peak, the city grew to a population of close to 700,000 in the 16th century, at the time second in size only to Beijing, and outgrown by London only in the course of the 18th century. When the Republic of Turkey was founded in 1923, the capital was moved from Constantinople to Ankara. Since 1930, the native name "Istanbul" has been the sole official name of the city in Turkish and has since replaced the traditional name "Constantinople" in most western languages as well.
